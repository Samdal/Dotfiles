    set_wallpaper(s)


    -- Each screen has its own tag table.
    if s.geometry.width >= s.geometry.height then
        awful.tag({"1", "2", "3", "4", "5", "6", "7", "8", "9"}, s, awful.layout.layouts[1])
      else
        awful.tag({"1", "2", "3", "4", "5", "6", "7", "8", "9"}, s, awful.layout.layouts[2])
